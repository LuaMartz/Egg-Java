
package FU_9;


public class Matematicas {
    
    //Atributos
    double a;
    double b;
       
    //constructor

    public Matematicas() {
    }

    public Matematicas(double a, double b) {
        this.a = a;
        this.b = b;
    }

    
    
    //Getter y setter

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    
    
}
